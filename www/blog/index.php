	<h1 class="storytitle" id="post-8"><a href="http://13thparallel.org/blog/2005/04/22/after-buzz/" rel="bookmark" title="Permanent Link: After Buzz">After Buzz</a></h1>
	<p class="author">22 April 2005</p>
	<div class="storycontent">
		<p>I guess one of the positives of &#8220;re-branding&#8221; an existing development technique is that it can push concepts into the wild that have otherwise remained low profile and unknown to many people.  So, while some of us who have been using such techniques may feel slightly irritated by the lack of recognition for <a href="http://webfx.eae.net/dhtml/xmlextras/xmlextras.html">existing</a> <a href="http://www.ashleyit.com/rs/main.htm">applications</a>, the uproar across the world of devloper blogs has definitely created a renewed focus on the capabilities of remote scripting.</p>

	<p>I&#8217;m sure something similar has been done before, but inspired by the recent debate Sean Treadway has produced this pretty sweet <a href=" http://sean.treadway.info/articles/2005/04/18/upload-progress-live-demo-plus-movie">Ajax powered file uploader</a> for <a href="http://www.rubyonrails.com/">RubyOnRails</a>.  The upload form posts to a hidden Iframe while Ajax requests the status from the server.  This technique could provide a nice addition to a <span class="caps">CMS</span> and I think I&#8217;ll be using something similar in an up coming project.</p>

	<p>My point in all this is that anything that promotes discussion and innovation is a good thing.  I miss the experimental endeavours of a few years ago when the boundaries were really being pushed on a daily basis.  Maybe the tides have turned and a new, more mature <a href="http://www.adaptivepath.com/publications/essays/archives/000430.php">period of inventiveness</a> is on the way.</p>	</div>

	
