	<h1 class="storytitle" id="post-2"><a href="http://13thparallel.org/blog/2005/01/16/good-intentions-and-best-laid-plans/" rel="bookmark" title="Permanent Link: Good intentions and best laid plans">Good intentions and best laid plans</a></h1>
	<p class="author">16 January 2005</p>
	<div class="storycontent">
		<p>It was just over three years ago that we launched the first issue of Thirteenth Parallel; an &#8220;e-zine&#8221; meant to showcase what could be done with <span class="caps">DHTML</span> while providing a place for people to learn about new technologies and practices.  We believed that the trend for sites which provided pre-made scripts wasn&#8217;t benefiting the fabled &#8220;web community&#8221;, we wanted to try and inspire people to learn the intricacies of the various web technologies and to come up with new, original and innovative ideas.  We were probably quite na&#239;ve, but some interesting stuff came out of it nevertheless.</p>

	<p>We ran seven issues before the real world caught up with us.  The amount of time it took to come up with enough ideas and write enough articles to fill the monthly issues was not realistic.  We were all working and studying hard and we just couldn&#8217;t find the time to keep it going.</p>

	<p>Over the years we have regularly come back and suggested ideas for how to rejuvenate the project.  On a number of occasions we actually started developing a new site, but we never found the time to complete it.</p>

	<p>So now, three years on, I have finally put the content back online as a <a href="/archive/">simple archive</a>; much of it should still be relevant, so I hope you find it useful and interesting.</p>

	<p>We still want to do something with Thirteenth Parallel eventually; so if you have any suggestions or wish to contribute, don&#8217;t hesitate to <a href="/contact/">get in touch</a>.</p>	</div>

	
