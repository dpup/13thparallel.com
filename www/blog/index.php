	<h1 class="storytitle" id="post-7"><a href="http://13thparallel.org/blog/2005/04/26/standardiseus/" rel="bookmark" title="Permanent Link: Standardise.Us">Standardise.Us</a></h1>

	<p class="author">26 April 2005</p>
	<div class="storycontent">
		<p>Back in 2000, during my BSc, I took a module on the semantic web.  One of the things I found most interesting was the concept of link-bases; hypertext systems which automatically cross-reference documents by associating keywords.  Links would not be many-to-one relationships but rather many-to-many.  In the standard web model many anchors each point to a single resource, in the proposed model a keyword within a document could point to several related resources (<a href="http://www.w3.org/XML/Linking"><span class="caps">XLINK</span> actually supports hyperlinking like this</a>).</p>

	<p>A typical implementation would have anchors added to important keywords, on rollover the user would then be shown a drop-down list of related content.   The problem is defining keywords for a document and deciding what to link.    There are various ways to automate this, many similar to how search engines rank content, but theoretically you could provide a link to every word on a page, and that is exactly what happens at <a href="http://www.standardise.us/">Standardise.Us</a>.</p>

	<p><a href="http://www.standardise.us/">Standardise.Us</a> is an interesting site that calls itsself a &#8220;hyper-threaded stream of consciousness&#8221;.  Entries are posted in the style of a blog and every word becomes a link to a &#8220;definition page&#8221;, then the definition links to other entries where it is used, ranked by relevence.  For example check out the entry for <a href="http://www.standardise.us/The-Information-Architecture-Institute">The Information Architecture Institute</a>.</p>	</div>
	
